    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int reponse = JOptionPane.showConfirmDialog(this, "Voulez vous vraiement modifier cette machine");
        if (reponse == 0) {
            if (mss.update(mss.findById(idd),new Machine(refTxt.getText(), marqueTxt.getText(), dateTxt.getDate(), Double.parseDouble(prixTxt.getText()),  (Employe)jComboBox1.getSelectedItem()))) {
                JOptionPane.showMessageDialog(this, "Modifié");
                loadMachines();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

