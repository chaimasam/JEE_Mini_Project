    void loadMachines() {
        modell.setRowCount(0);
        for (Machine m : mss.findAll()) {
            modell.addRow(new Object[]{
                m.getId(),
                m.getReference(),
                m.getMarque(),
                m.getDateAchat(),
                m.getPrix()
            });
        }
    }
    private void updateCombo(){
       for (Employe e : ess.findAll()) {
           jComboBox1.addItem(e);
       }
    
    }

